package com.csu.assignments;

public class forLoops {

	public static void main(String[] args) {
		for (int i=0; i<=1000000 ;i++) {
			
			if(i==10) {
				System.out.println(i);
			}else if (i==100) {
				System.out.println(i);
			}else if (i==1000) {
				System.out.println(i);
			}else if (i==10000) {
				System.out.println(i);
			}else if (i==100000) {
				System.out.println(i);
			}else if (i==1000000) {
				System.out.println(i);
			}	
		}
	}
}
